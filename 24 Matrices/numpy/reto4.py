import numpy as np
matriz = np.array([
    [3,4,6,3,2],
    [5,4,3,2,2],
    [8,56,34,2,1]
])

# La función .ndim nos dice cuántas dimensiones tiene el arreglo:
print(matriz.ndim)

# La función .shape nos dice de cuántas filas y columnas se compone el arreglo:
print(matriz.shape)

# La función .size nos dice de cuántos elementos se compone el arreglo:
print(matriz.size)