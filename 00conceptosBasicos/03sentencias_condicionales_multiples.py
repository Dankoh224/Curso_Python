print("Convertidor de números a letras.")

num1 = int(input("Ingresa el primer valor que quieres convertir: "))

if num1 == 1:
    print("El número es UNO")
elif num1 == 2:
    print("El número es DOS")
elif num1 == 3:
    print("El número es TRES")
elif num1 == 4:
    print("El número es CUATRO")
elif num1 == 5:
    print("El número es CINCO")
else:
    print("Estre programa solo puede transformar números hasta 5.")

print("Fin.")
