print("escribe los números a comparar:")

num1 = input("Ingresa el primer número: ")
num2 = input("Ingresa el segundo número: ")

if num1 == num2:
    print("Los números que ingresaste son iguales.")

if num1 != num2:
    print("Los números que ingresaste son distintos.")

if num1 < num2:
    print("El primer valor es menor que el segundo valor.")

if num1 > num2:
    print("El primer valor es mayor que el segundo valor.")

if num1 <= num2:
    print("El primer valor es menor o igual que el segundo valor.")

if num1 >= num2:
    print("El primer valor es mayor o igual que el segundo valor.")

print("Fin del programa.")
